//Uma maneira bem curiosa de somar um valor de uma variável por ela mesma seria usando o operador +=, veja o exemplo abaixo.

let valorTecladoGamer = 100;
console.log(valorTecladoGamer += valorTecladoGamer); // Desse modo você evita ter que somar duas vezes o valor da variável e ter que passar esse resultado para uma outra variável.

//O mesmo você pode fazer com o valor - no lugar do + e assim ele irá realizar a operação subraindo e não mais somando e todos os outros operadores funciona de forma igual, ou seja, você pode usar (+=), (-=), (/=), (*=), (**=).
let valorTecladoGamer2 = 3;

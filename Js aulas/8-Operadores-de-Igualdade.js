//Operadores de igualdades
//Igualdade estrita: Esses operadores irão confirmar e trazer com resultados de Verdadeiro e falso uma determina operação, ex.

console.log(1 === 1); // ESSA É A MAIS RECOMENDÁVEL: Aqui ele além de confirmar que o primeiro valor é igual ao segundo, também está confirmando que ambos sao do mesmo tipo.

 //Igualdade solta
 console.log(1 == 1);//essa é uma igualdade menos restrita ele não comparará o tipo das variaveis apenas os valores.

 //Ex.
 console.log('1' == 1);// Ele ignora que estamos trabalhando com dois tipos de variáveis diferentes, apenas compara os valores.


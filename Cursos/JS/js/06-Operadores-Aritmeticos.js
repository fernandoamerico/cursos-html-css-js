//##Operadores Aritiméticos:

let salario = 100;

// Podemos usar: +, -, *, / , **(Exponencial[Numero vezes ele mesmo]).
console.log(salario + salario);
console.log(salario - salario);
console.log(salario * salario);
console.log(salario / salario);
console.log(salario ** salario); //outro exemplo abaixo:
console.log(5 ** 5);

// // Também temos o ++ e o -- (Incremento e decremento);

let idade = 18;

// Em outras palavras o mais mais adiciona +1 e o -- tira 1, mas no console.log você deve colocar o ++ antes da variável para que seja mostrado na tela o resultado, caso contrário a soma será feita depois de mostrado na tela o valor da variável. Ex.

console.log(idade++);
console.log(idade);

//Mas se você por o ++ ou o -- antes da variável então ele rearizará o processo e depois mostrar na tela, veja:

console.log(--idade);
console.log(idade);


// Outros Operadores
// += (Somo um numero variável mais o valor da própria variável);
a += 5;

// é igual a:
a + 5;

// Ou por exemplo: a -= 10 que seria o mesmo de a - 10;
// Ou seja, será sempre o próprio numero somado (ou dividio ou mutiplicado e etc) por o numero indicado.